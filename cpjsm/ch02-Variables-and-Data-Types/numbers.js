/* Numbers - JS is dynamically typed,
so we dont have to specify if number is an int or float.*/

//in JavaScript:
const wholeNumber = 26; //Integer
const decimalNumber = 3.14; //float

/* 
In other language like C we would need to specify:
int wholeNumber = 26;
float decimalNumber = 3.14; 
*/

//can perform all the standard math operations:

const a = 10;
const b = 25;

console.log(a + b); //addition
console.log(a - b); //subtraction
console.log(a * b); //multiplication
console.log(a / b); //division

//Nan is short for Not a Number, it is a special value that represents an invalid number/

